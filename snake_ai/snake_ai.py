        head_x, head_y = self.snake[0]
        food_x, food_y = self.food

        # Simple AI: move towards food
        if food_x > head_x and self.direction != 'LEFT':
            return 'RIGHT'
        elif food_x < head_x and self.direction != 'RIGHT':
            return 'LEFT'
        elif food_y > head_y and self.direction != 'UP':
            return 'DOWN'
        elif food_y < head_y and self.direction != 'DOWN':
            return 'UP'
        
        # If direct path is blocked, try other directions
        # This is a very basic fallback and might lead to collisions
        possible_directions = ['UP', 'DOWN', 'LEFT', 'RIGHT']
        current_direction_idx = possible_directions.index(self.direction)
        
